// login and logout

async function login(){

    if(window.ethereum){

      permissions()

      changeChain()

    }else{
      console.log('install metamask')
    }
  
  
}

function logout(){
    
    popup.classList.remove('visible')
    itemConect.classList.remove('walletAddress')
    itemConect.innerHTML = 'Conect Wallet'

}

